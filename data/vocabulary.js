// 辅助函数
const VocabularyAPI = {
    // 根据关卡获取单词
    getWordsByLevel: function(level) {
        return VOCABULARY.filter(word => word.level === level);
    },

    // 根据ID获取单词
    getWordById: function(id) {
        return VOCABULARY.find(word => word.id === id);
    },

    // 获取随机错误选项
    getRandomWrongOptions: function(correctWord, count = 3) {
        const wrongWords = VOCABULARY.filter(word =>
            word.id !== correctWord.id && word.meaning !== correctWord.meaning
        );

        const shuffled = wrongWords.sort(() => 0.5 - Math.random());
        return shuffled.slice(0, count).map(word => word.meaning);
    },

    // 获取所有单词总数
    getTotalWordsCount: function() {
        return VOCABULARY.length;
    },

    // 获取指定数量的随机单词
    getRandomWords: function(count) {
        const shuffled = [...VOCABULARY].sort(() => 0.5 - Math.random());
        return shuffled.slice(0, count);
    }
};